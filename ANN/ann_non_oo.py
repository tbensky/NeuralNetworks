def activate(x):
    return 1/(1+math.exp(-x))

def activatep(x):
    return activate(x) * (1.0-activate(x))


def clear_dw(NN):
    for layer in range(len(NN)-1):
        for neuron in range(len(NN[layer])):
            NN[layer][neuron]['dw'] = [0] * len(NN[layer+1])
            NN[layer][neuron]['db'] = 0.0

def forward(NN,x):
    assert len(x) == len(NN[0])
    
    #load up input layer
    input_layer = 0
    for neuron in range(len(x)):
        NN[input_layer][neuron]['z'] = x[neuron]
        NN[input_layer][neuron]['a'] = activate(x[neuron])

    #propagate from input layer through the entire network
    for layer in range(len(NN)-1):
        forward_layer = layer + 1
        for forward_neuron in range(len(NN[forward_layer])):
            z = 0.0
            for neuron in range(len(NN[layer])):
                z = z + NN[layer][neuron]['w'][forward_neuron] * NN[layer][neuron]['a'] + NN[layer][neuron]['b']
            NN[forward_layer][forward_neuron]['z'] = z
            NN[forward_layer][forward_neuron]['a'] = activate(z)

    return([x['a'] for x in NN[forward_layer]])

def backward(NN,y):
    #compute all of the deltas for
    #1) output layer
    output_layer = len(NN)-1
    for neuron in range(len(NN[output_layer])):
        z = NN[output_layer][neuron]['z']
        a = NN[output_layer][neuron]['a']
        dL = a - y[neuron]
        NN[output_layer][neuron]['delta'] = dL * activatep(z)
        NN[output_layer][neuron]['db'] += ETA_B *  NN[output_layer][neuron]['delta']
   
    #2) hidden layers
    for hidden_layer in range(output_layer-1,-1,-1):
        forward_layer = hidden_layer + 1
        for neuron in range(len(NN[hidden_layer])):
            dsum = 0.0
            for forward_neuron in range(len(NN[forward_layer])):
                w = NN[hidden_layer][neuron]['w'][forward_neuron]
                delta_forward_layer = NN[forward_layer][forward_neuron]['delta']
                dsum = dsum + delta_forward_layer * w
            z = NN[hidden_layer][neuron]['z']
            NN[hidden_layer][neuron]['delta'] = activatep(z) * dsum

    #adjust all weights
    for layer in range(output_layer-1,-1,-1):
        for neuron in range(len(NN[layer])):
            NN[layer][neuron]['db'] += ETA_B *  NN[layer][neuron]['delta']
            a = NN[layer][neuron]['a']
            for forward_neuron in range(len(NN[layer+1])):
                delta = NN[layer+1][forward_neuron]['delta']
                #NN[L][x]['w'][y] = NN[L][x]['w'][y] - ETA * delta * a
                NN[layer][neuron]['dw'][forward_neuron] += ETA * delta * a

def adjust_weights(NN,data_count):
    #adjust all weights
    L = len(NN)-2
    while L >= 0:
        for x in range(len(NN[L])):
            for y in range(len(NN[L+1])):
                # -= due to "gradient descent." 
                NN[L][x]['w'][y] -= NN[L][x]['dw'][y] / data_count
        L = L - 1

def adjust_biases(NN,data_count):
    #adjust all biases
    for layer in range(len(NN)):
        for neuron in range(len(NN[layer])):
            # -= due to "gradient descent." 
            NN[layer][neuron]['b'] -= NN[layer][neuron]['db'] / data_count

